package com.jspl.lms.dto;

import java.util.Date;

public class ReportParam {
	
	private Integer locationPkId;
	private Date date;
	private Double quantity;
	private String roleId;
	private Integer userPkId;
	
	public Date getDate() {
		return date;
	}
	public void setDate(Date date) {
		this.date = date;
	}
	public Double getQuantity() {
		return quantity;
	}
	public void setQuantity(Double quantity) {
		this.quantity = quantity;
	}
	public String getRoleId() {
		return roleId;
	}
	public void setRoleId(String roleId) {
		this.roleId = roleId;
	}
	public Integer getUserPkId() {
		return userPkId;
	}
	public void setUserPkId(Integer userPkId) {
		this.userPkId = userPkId;
	}
	public Integer getLocationPkId() {
		return locationPkId;
	}
	public void setLocationPkId(Integer locationPkId) {
		this.locationPkId = locationPkId;
	}
	
}
