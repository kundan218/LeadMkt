package com.jspl.lms.dto;

import java.util.List;

public class SalesReport {
	
	private String[] regionArray;
	private String[] dayArray;
	private List<Sales> saleList;
	
		
	public String[] getRegionArray() {
		return regionArray;
	}
	public void setRegionArray(String[] regionArray) {
		this.regionArray = regionArray;
	}
	public String[] getDayArray() {
		return dayArray;
	}
	public void setDayArray(String[] dayArray) {
		this.dayArray = dayArray;
	}
	public List<Sales> getSaleList() {
		return saleList;
	}
	public void setSaleList(List<Sales> saleList) {
		this.saleList = saleList;
	}
	
	
	
	

}
